# Description: Calculate area and Circumference of a Circle
import math
radius = 3
area = round(math.pi * radius**2,1)
circumference = round(2 * math.pi * radius,1)
print ("A circle with radius",radius,"cm has an area of",area,"square centimetres and a circumference of",circumference,"cm.")